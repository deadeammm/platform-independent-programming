# Лабораторная работа №1
# Программа "Личная визитка"

# Константа с текущим годом.
# При необходимости можно изменить значение на актуальный год.
CURRENT_YEAR = 2026

# Запрос имени пользователя
first_name = input("Введите ваше имя: ")

# Запрос фамилии пользователя
last_name = input("Введите вашу фамилию: ")

# Запрос года рождения.
# Функция int() преобразует введённую строку в целое число.
birth_year = int(input("Введите год рождения: "))

# Запрос роста.
# Функция float() преобразует введённую строку в число с плавающей точкой.
height = float(input("Введите ваш рост (см): "))

# Вычисление возраста пользователя
age = CURRENT_YEAR - birth_year

# Ширина рамки для красивого вывода
width = 40

# Вывод верхней части рамки и заголовка
print("\n" + "*" * width)
print("*{:^38}*".format("Личная визитка"))
print("*" * width)

# Пустая строка для отделения заголовка от ввода
print()

# Вывод результата
print("*" * width)
print("*{:^38}*".format("ВАША ВИЗИТКА"))
print("*" * width)

# Форматированный вывод данных пользователя
print("* {:<36} *".format(f"Имя: {first_name}"))
print("* {:<36} *".format(f"Фамилия: {last_name}"))
print("* {:<36} *".format(f"Год рождения: {birth_year}"))
print("* {:<36} *".format(f"Возраст: {age} лет"))
print("* {:<36} *".format(f"Рост: {height} см"))

# Нижняя граница рамки
print("*" * width)